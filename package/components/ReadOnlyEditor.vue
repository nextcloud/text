<template>
	<EditorContent v-if="editor" id="read-only-editor" :editor="editor" />
</template>

<script>
import { Editor } from '@tiptap/core'
import { PlainText, RichText } from './../extensions'
import { EditorContent } from '@tiptap/vue-2'
import escapeHtml from 'escape-html'
import markdownit from './../markdownit'

export default {
	name: 'ReadOnlyEditor',
	components: { EditorContent },
	inject: {
		extensions: { default: [] },
	},
	props: {
		content: {
			type: String,
			required: true,
		},
		isRichEditor: {
			type: Boolean,
			default: true,
		},
		richTextOptions: {
			type: Object,
			default: () => {},
		},
	},
	data: () => {
		return {
			editor: null,
		}
	},
	mounted() {
		this.editor = this.isRichEditor
			? this.createRichEditor()
			: this.createPlainEditor()
		this.editor.setOptions({ editable: false })
		this.$nextTick(() => {
			this.preventOpeningLinks()
		})
	},
	beforeDestroy() {
		this.editor.destroy()
	},
	methods: {

		createRichEditor() {
			const content = markdownit.render(this.content)
			return new Editor({
				content,
				extensions: [
					RichText.configure({
						...this.richTextOptions,
						link: {
							onClick: (event, attrs) => this.$emit('click-link', event, attrs),
						},
					}),
					...this.extensions,
				],
			})
		},

		createPlainEditor() {
			const content = '<pre>' + escapeHtml(this.content) + '</pre>'
			return new Editor({
				content,
				extensions: [PlainText],
			})
		},

		/* Stop the browser from opening links.
		 * Clicks are handled inside the Link mark just like in edit mode.
		 */
		preventOpeningLinks() {
			this.$el.addEventListener('click', event => {
				if (event.target.closest('a')) {
					event.preventDefault()
				}
			})
		},
	},
}
</script>

